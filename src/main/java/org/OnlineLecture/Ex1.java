package org.example;


public class Ex1 {

    public static void main(String[] args) {
// result changes when changing the numbers.
        int x = 10;
        int y = 5;

        if (x > y) {
            System.out.println("x is bigger then y");
        }
        else if (x < y) {
            System.out.println("x is smaller then y");
        }
        else {
            System.out.println("x and y are the same");

        }
    }



}
